package Datatypes;
public class Basicdatatypes{
    public static void main(String[] args) {
        int age = 20; // Integer
        float height = 5.8f; // Float
        char grade = 'A'; // Character

        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Grade: " + grade);
    }
}

