package array;
    public class IfElseExample {
        public static void main(String[] args) {
            // Defining a number
            int number = -5;
    
            // If-else statement
            if (number > 0) {
                System.out.println("The number is positive.");
            } else if (number < 0) {
                System.out.println("The number is negative.");
            } else {
                System.out.println("The number is zero.");
            }
        }
    }
    
