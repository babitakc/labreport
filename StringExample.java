package array;

public class StringExample {
        public static void main(String[] args) {
            // Defining a string
            String message = "Hello, Java World!";
    
            // Using string methods
            System.out.println("Original message: " + message);
            System.out.println("Length of the string: " + message.length());
            System.out.println("Uppercase: " + message.toUpperCase());
            System.out.println("Substring (from index 7): " + message.substring(7));
            System.out.println("Replacing 'Java' with 'C++': " + message.replace("Java", "C++"));
        }
    }
    
    

